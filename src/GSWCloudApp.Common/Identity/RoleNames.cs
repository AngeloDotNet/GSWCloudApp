namespace GSWCloudApp.Common.Identity;

/// <summary>
/// Contains the role names used for user authorization.
/// </summary>
public static class RoleNames
{
    /// <summary>
    /// Role name for administrators.
    /// </summary>
    public const string Administrator = nameof(Administrator);

    /// <summary>
    /// Role name for power users.
    /// </summary>
    public const string PowerUser = nameof(PowerUser);

    /// <summary>
    /// Role name for regular users.
    /// </summary>
    public const string User = nameof(User);
}
