namespace GSWCloudApp.Common.Constants;

/// <summary>
/// Provides a set of constant strings representing identity claims.
/// </summary>
public static class IdentityClaims
{
    /// <summary>
    /// Claim type for the full name of the user.
    /// </summary>
    public const string FullName = nameof(FullName);

    /// <summary>
    /// Claim type for the username of the user.
    /// </summary>
    public const string Username = nameof(Username);

    /// <summary>
    /// Claim type for the license of the user.
    /// </summary>
    public const string License = nameof(License);

    /// <summary>
    /// Claim type for the permesso of the user.
    /// </summary>
    public const string Permesso = nameof(Permesso);

    /// <summary>
    /// Claim type for the modulo of the user.
    /// </summary>
    public const string Modulo = nameof(Modulo);
}
